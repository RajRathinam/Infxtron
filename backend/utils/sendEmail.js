// utils/sendEmail.js
import nodemailer from "nodemailer";

export const sendEmail = async ({ to, subject, html }) => {
  try {
    // Use the same Gmail service configuration that's working in OTP emails
    const transporter = nodemailer.createTransport({
      service: "gmail",
      auth: {
        user: process.env.EMAIL_USER,  // Use EMAIL_USER instead of SMTP_USER
        pass: process.env.EMAIL_PASS,  // Use EMAIL_PASS instead of SMTP_PASS
      },
    });

    const info = await transporter.sendMail({
      from: `"AG's Healthy Food" <${process.env.EMAIL_USER}>`,
      to,
      subject,
      html,
    });

    console.log("Email sent successfully: %s", info.messageId);
    return info;
  } catch (err) {
    console.error("Email sending failed:", err);
    throw err;
  }
};